package example_pojo;

public class Book {
	
		private String Book_Title;
		private int Pages_in_Book;
		private String Best_Quote;
		public String getBook_Title() {
			return Book_Title;
		}
		public void setBook_Title(String book_Title) {
			Book_Title = book_Title;
		}
		public int getPages_in_Book() {
			return Pages_in_Book;
		}
		public void setPages_in_Book(int pages_in_Book) {
			Pages_in_Book = pages_in_Book;
		}
		public String getBest_Quote() {
			return Best_Quote;
		}
		public void setBest_Quote(String best_Quote) {
			Best_Quote = best_Quote;
		}
}
